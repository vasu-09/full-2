package com.om.Notification_Service.service;

import com.om.Notification_Service.dto.EventMessage;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.mail.SimpleMailMessage;
import org.springframework.mail.javamail.JavaMailSender;
import org.springframework.stereotype.Service;

@Service
public class EmailSender {

    private final JavaMailSender mailSender;

    @Autowired
    public EmailSender(JavaMailSender mailSender) {
        this.mailSender = mailSender;
    }

    public void sendMeetingReminder(EventMessage event) {
        SimpleMailMessage message = new SimpleMailMessage();
        message.setTo("recipient@example.com"); // Look up user email by userId
        message.setSubject("Meeting Reminder");
        message.setText("You have a meeting scheduled at: " + event.getData().get("startTime"));

        mailSender.send(message);
    }

    // More methods for password reset, chat alert, etc.
}

